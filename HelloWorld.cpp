// BASIC LINE PRINTING PROGRAM
// Below is a preprocessor directive 
# include <iostream>

// calls the standard namespace library
using namespace std;

int main()
{

// cout << is used to print out text in quotation marks
    cout << "Hello world!" << endl;

// \n can be used to skip lines	
    cout << "Skipped a new line \n\n";
    cout << "And me";

// return 0 is then used to end a program
    return 0;
}

